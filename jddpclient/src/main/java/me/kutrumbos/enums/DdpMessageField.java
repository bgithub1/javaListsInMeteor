package me.kutrumbos.enums;

/**
 * Enum corresponding to the valid fields of a DDP message
 * @author Pete
 *
 */
public enum DdpMessageField {
	// general fields
	msg, id, params, error,
	// fields related to DDP connection messages
	session, version, support, 
	// fields related to managing subscription data
	name, collection, fields, cleared, subs, before,
	// fields related to remote method invocation
	method, methods, result;
	

}
